uint32_t map(uint32_t x, uint32_t in_min, uint32_t in_max, uint32_t out_min, uint32_t out_max){
	return (x - in_min) * (out_max - out_min)/(in_max - in_min) + out_min;
}


void servo_Write(Servo *sv, uint8_t angle){
	uint16_t ccr = map(angle, 0, 180, 450, 2400);
	sv->angle = angle;
	
	switch(sv->Channel){
		case TIM_CHANNEL_1:
			sv->htim->Instance->CCR1 = ccr;
		break;
		case TIM_CHANNEL_2:
			sv->htim->Instance->CCR2 = ccr;
		break;
		case TIM_CHANNEL_3:
			sv->htim->Instance->CCR3 = ccr;
		break;
		case TIM_CHANNEL_4:
			sv->htim->Instance->CCR4 = ccr;
		break;
	}
}

void Servo_Init(Servo *sv, TIM_HandleTypeDef *htim, uint32_t Channel){
	sv->htim = htim;
	sv->Channel = Channel;
	sv->angle = 0;
	
	htim->Instance->PSC = 71;
	htim->Instance->ARR = 19999;
	HAL_TIM_PWM_Start(htim, Channel);
}
